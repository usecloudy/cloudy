CREATE OR REPLACE FUNCTION "public"."match_thought_chunks"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "exclude_thought_id" "uuid", "input_author_id" "uuid") RETURNS SETOF "public"."thought_embeddings"
    LANGUAGE "sql"
    AS $$
  select te.*
  from thought_embeddings te
  join thoughts t on te.thought_id = t.id
  where te.embedding <#> query_embedding < -match_threshold
    and t.author_id = input_author_id
    and te.thought_id != exclude_thought_id
  order by te.embedding <#> query_embedding asc
  limit least(match_count, 200);
$$;

