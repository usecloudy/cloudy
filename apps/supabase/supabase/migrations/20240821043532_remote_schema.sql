CREATE OR REPLACE FUNCTION "public"."match_thought_chats"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "thought_id" "uuid") RETURNS SETOF "public"."thought_chats"
    LANGUAGE "sql"
    AS $$
  select *
  from thought_chats
  where thought_chats.embedding <#> query_embedding < -match_threshold
  and thought_chats.thought_id = thought_id
  order by thought_chats.embedding <#> query_embedding asc
  limit least(match_count, 200);
$$;

