CREATE OR REPLACE FUNCTION "public"."match_thoughts"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "author_id" "uuid") RETURNS TABLE("thought_id" "uuid", "similarity" double precision)
    LANGUAGE "sql"
    AS $$
  select 
    t.id as thought_id,
    (te.embedding <=> query_embedding) as similarity
  from thought_embeddings te
  join thoughts t on te.thought_id = t.id
  where te.embedding <=> query_embedding < -match_threshold
    and t.author_id = author_id
  order by te.embedding <=> query_embedding asc
  limit least(match_count, 200);
$$;

ALTER FUNCTION "public"."match_thoughts"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "author_id" "uuid") OWNER TO "postgres";

CREATE OR REPLACE FUNCTION "public"."match_thoughts"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "author_id" "uuid", "collection_id" "uuid") RETURNS TABLE("thought_id" "uuid", "similarity" double precision)
    LANGUAGE "sql"
    AS $$
  select 
    t.id as thought_id,
    1 - (te.embedding <#> query_embedding) as similarity
  from thought_embeddings te
  join thoughts t on te.thought_id = t.id
  join collection_thoughts ct on ct.thought_id = t.id
  where te.embedding <#> query_embedding < -match_threshold
    and t.author_id = author_id
    and ct.collection_id = collection_id
  order by te.embedding <#> query_embedding asc
  limit least(match_count, 200);
$$;

